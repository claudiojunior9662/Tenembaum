    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        //INSTANCIA AS CLASSES--------------------------------------------------
        FuncionariosDAO fDAO = new FuncionariosDAO();
        DefaultTableModel modeloConsulta = (DefaultTableModel) tabelaPesquisa.getModel();
        modeloConsulta.setNumRows(0);
        //VERIFICA SE O CAMPO FOI PREENCHIDO CORRETAMENTE-----------------------
        if(campoMatricula.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Matricula'");
            return;
        }
        //FAZ A PESQUISA NO BANCO E PREENCHE TABELA-----------------------------
        String matricula = campoMatricula.getText().replace(" ", "");
        for(FuncionariosBEAN fBEAN : fDAO.retornaPesquisa(matricula)){
            modeloConsulta.addRow(new Object[]{
                matricula,
                fBEAN.getNome(),
                fBEAN.getSetor(),
                fBEAN.getCargo()
            });
        }
    }//GEN-LAST:event_botaoPesquisarActionPerformed

