    private void botaoSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarAlteracoesActionPerformed
        //INSTANCIAR AS FUNÇÕES NECESSÁRIAS-------------------------------------
        FuncionariosDAO fDAO = new FuncionariosDAO();
        FuncionariosBEAN fBEAN = new FuncionariosBEAN();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        //VERIFICA OS ERROS DE INSERÇÃO DE DADOS--------------------------------
        if(campoMatricula.getText().replace(" ", "").equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Matricula'");
            return;
        }
        if(campoNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Nome Completo'");
            return;
        }
        if(campoCpf.getText().replace(".", "").replace("-", "").replace(" ", "").equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'CPF'");
            return;
        }
        if(campoRg.getText().replace(".", "").replace("-", "").equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'RG'");
            return;
        }
        if(campoDataNascimento.getDate().equals(null)){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Data de nascimento'");
            return;
        }
        if(campoDataAdmissao.getDate().equals(null)){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Data de admissão'");
            return;
        }
        if(comboSetor.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Setor'");
            return;
        }   
        if(campoCargo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Cargo'");
            return;
        }
        if(campoSenha.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Senha de login'");
            return;
        }
        if(campoEndereco.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Endereço'");
            return;
        }
        if(campoBairro.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Bairro'");
            return;
        }
        if(campoRua.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'Rua'");
            return;
        }
        if(campoCEP.getText().replace(".", "").replace("-", "").equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo 'CEP'");
            return;
        }
        //VERIFICA SE FOI SELECIONADO ALGUM DIA---------------------------------
        if(segunda.isSelected() == false && terca.isSelected() == false && quarta.isSelected() == false &&
            quinta.isSelected() == false && sexta.isSelected() == false && sabado.isSelected() == false &&
            domingo.isSelected() == false){
            JOptionPane.showMessageDialog(null, "Preencha os dias em que o funcionário irá trabalhar");
            return;
        }
        //VERIFICA SE OS HORÁRIOS FORAM INSERIDOS-------------------------------
        if(segunda.isSelected() == true){
            if(fb.getHorarioSegunda() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Segunda'");
                return;
            }else if(fb.getHorarioSegunda().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Segunda'");
            }
        }if(terca.isSelected() == true){
            if(fb.getHorarioTerca() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Terça'");
                return;
            }else if(fb.getHorarioTerca().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Terça'");
                return;
            }
        }if(quarta.isSelected() == true){
            if(fb.getHorarioQuarta() == null){
                JOptionPane.showMessageDialog(null, "Insira  o horário para 'Quarta'");
                return;
            }else if(fb.getHorarioQuarta().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Quarta'");
            }
        }if(quinta.isSelected() == true){
            if(fb.getHorarioQuinta() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Quinta'");
                return;
            }else if(fb.getHorarioQuinta().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Quinta'");
                return;
            }
        }if(sexta.isSelected() == true){
            if(fb.getHorarioSexta() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Sexta'");
                return;
            }else if(fb.getHorarioSexta().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Sexta'");
                return;
            }
        }if(sabado.isSelected() == true){
            if(fb.getHorarioSabado() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Sábado'");
                return;
            }else if(fb.getHorarioSabado().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inserido para 'Sábado'");
                return;
            }
        }if(domingo.isSelected() == true){
            if(fb.getHorarioDomingo() == null){
                JOptionPane.showMessageDialog(null, "Insira o horário para 'Domingo'");
                return;
            }else if(fb.getHorarioDomingo().length() < 11){
                JOptionPane.showMessageDialog(null, "Verifique o horário inseido para 'Domingo'");
                return;
            }
        }
        //VERIFICA REPETIÇÃO DE DADOS-------------------------------------------
        for(FuncionariosBEAN aux : fDAO.verificaRepeticao()){
            if(aux.getMatricula().equals(campoMatricula.getText().replace(" ", ""))){
                JOptionPane.showMessageDialog(null, "A matrícula digitada já existe.");
                return;
            }
            if(aux.getCpf().equals(campoCpf.getText().replace(".", "").replace("-", "").replace(" ", ""))){
                JOptionPane.showMessageDialog(null, "O CPF digitado já existe.");
                return;
            }
            if(aux.getRg().equals(campoRg.getText().replace(".", "").replace("-", "").replace(" ", ""))){
                JOptionPane.showMessageDialog(null, "O RG digitado já existe");
                return;
            }
        }
        //PREENCHE BEAN---------------------------------------------------------
        fBEAN.setMatricula(campoMatricula.getText().replace(" ", ""));
        fBEAN.setNome(campoNome.getText());
        fBEAN.setCpf(campoCpf.getText().replace(".", "").replace("-", "").replace(" ", ""));
        fBEAN.setRg(campoRg.getText().replace(".", "").replace("-", "").replace(" ", ""));
        fBEAN.setDataNascimento(sdf.format(campoDataNascimento.getDate()));
        fBEAN.setDataAdmissao(sdf.format(campoDataAdmissao.getDate()));
        fBEAN.setSetor(comboSetor.getSelectedItem().toString());
        fBEAN.setCargo(campoCargo.getText());
        fBEAN.setSenha(campoSenha.getText());
        fBEAN.setEndereco(campoEndereco.getText());
        fBEAN.setBairro(campoBairro.getText());
        fBEAN.setRua(campoRua.getText());
        fBEAN.setCep(campoCEP.getText().replace(".", "").replace("-", "").replace(" ", ""));
        
        String horarioDefinitivo = null;
        if(domingo.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "1" + fb.getHorarioDomingo();
        }if(segunda.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "2" + fb.getHorarioSegunda();
        }if(terca.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "3" + fb.getHorarioTerca();
        }if(quarta.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "4" + fb.getHorarioQuarta();
        }if(quinta.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "5" + fb.getHorarioQuinta();
        }if(sexta.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "6" + fb.getHorarioSexta();
        }if(sabado.isSelected() == true){
            horarioDefinitivo = horarioDefinitivo + "-" + "7" + fb.getHorarioSabado();
        }
        //SALVA NO BANCO DE DADOS-----------------------------------------------
        fDAO.createFuncionario(fBEAN);
        //EXIBE MENSAGEM DE CONFIRMAÇÃO-----------------------------------------
        JOptionPane.showMessageDialog(null, "A matrícula " + campoMatricula.getText() + " foi adicionada com sucesso.");
        //VOLTA AO ESTADO INICIAL-----------------------------------------------
        estadoSalvarAlteracoes();
    }//GEN-LAST:event_botaoSalvarAlteracoesActionPerformed

