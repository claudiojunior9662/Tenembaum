    public void atualizaComboDia(){
        comboDia.removeAllItems();
        comboDia.addItem("Selecione...");
        if(fb.getSegunda() == 1){comboDia.addItem("Segunda");}
        if(fb.getTerca() == 1){comboDia.addItem("Terça");}
        if(fb.getQuarta() == 1){comboDia.addItem("Quarta");}
        if(fb.getQuinta() == 1){comboDia.addItem("Quinta");}
        if(fb.getSexta() == 1){comboDia.addItem("Sexta");}
        if(fb.getSabado() == 1){comboDia.addItem("Sábado");}
        if(fb.getDomingo() == 1){comboDia.addItem("Domingo");}
    }
